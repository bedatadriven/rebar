package com.bedatadriven.rebar.time;

public class CalendricalException extends RuntimeException {

	public CalendricalException() {
		super();
	}

	public CalendricalException(String message, Throwable cause) {
		super(message, cause);
	}

	public CalendricalException(String message) {
		super(message);
	}

	public CalendricalException(Throwable cause) {
		super(cause);
	}

}
